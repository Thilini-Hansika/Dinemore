    @Override
    public void update() throws RemoteException {
        try {
            // DefaultTableModel dtm = new DefaultTableModel();
            dtm.setRowCount(0);
            java.util.List<OrdersDTO> allList = OrderController.getAllList();
            for (OrdersDTO o : allList) {
                Object[] rowdata = {o.getOid(), o.getCustomerName(), o.getPhone(), o.getQty()};
                dtm.addRow(rowdata);
            }
        } catch (Exception ex) {
            Logger.getLogger(ChefForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadSystemDate() {
        Date date = new Date();
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("YYYY-MM-dd");
        String format = simpleDateFormat.format(date);
        txtDate.setText(format);
    }

    private void loadChefCombo() {
        try {
            java.util.List<AddChefDTO> allChefs = ChefController.getAllChef();
            for (AddChefDTO allChef : allChefs) {
                cmbChefName.addItem(allChef.getChefName());

            }
        } catch (Exception ex) {
            Logger.getLogger(ChefForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
