    private void btnFinishedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinishedActionPerformed

        int oid = Integer.parseInt(txtOid.getText());
        int chefId = Integer.parseInt(txtChefId.getText());
        String name = txtCusName.getText();

        int time = Integer.parseInt(txtTime.getText());
        String date = txtDate.getText();
        String bun = txtAty.getText();

        ChefDTO chef = new ChefDTO();
        chef.setOid(oid);
        chef.setChefID(chefId);
        chef.setDate(date);
        chef.setCustomerName(name);

        chef.setPreparedBun(bun);

        chef.setSpentTime(time);
        try {
            boolean isSave = ChefController.addOrder(chef);
           if (isSave) {
                JOptionPane.showMessageDialog(this, "Chef Order Added");
            } else {
                JOptionPane.showMessageDialog(this, "Sorry Unable to add this Order");
            }

        } catch (Exception ex) {
            Logger.getLogger(ChefForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnFinishedActionPerformed

    private void cmbChefNameItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbChefNameItemStateChanged
        try {
            if (cmbChefName.getSelectedItem() == null) {
                return;
            }
            String chefName = cmbChefName.getSelectedItem().toString();
            AddChefDTO searchChefByName = ChefController.searchChefByName(chefName);

            txtChefId.setText(Integer.toString(searchChefByName.getChefId()));
        } catch (Exception ex) {
            Logger.getLogger(ChefForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_cmbChefNameItemStateChanged

